            /// <summary>
            /// text/javascript
            /// </summary>
            public const string Javascript = Prefix + "javascript";

            /// <summary>
            /// text/plain
            /// </summary>
            public const string Plain = Prefix + "plain";

            /// <summary>
            /// text/richtext
            /// </summary>
            public const string RichText = Prefix + "richtext";

            /// <summary>
